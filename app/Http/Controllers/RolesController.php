     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        if($request->input('page') && $request->input('page')=='all'){
            return Role::all();
        }
        return Role::paginate($request->input('page_size') ?? 15);
    }

